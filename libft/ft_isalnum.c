int	ft_isalnum(int c)
{
	int	is_alnum;

	is_alnum = 0;
	if ((c >= '0' && c <= '9') || (c >= 'a' && c <= 'z') \
			|| (c >= 'A' && c <= 'Z'))
		is_alnum = 1;
	return (is_alnum);
}
