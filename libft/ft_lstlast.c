#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	int	l;
	int	i;

	i = 0;
	l = ft_lstsize(lst);
	while (i < l - 1)
	{
		lst = lst->next;
		i++;
	}
	return (lst);
}
